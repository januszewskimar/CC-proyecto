class Usuario{
	nombreUsuario: string;
	correo: string;
	nombre: string;
	apellidos: string;
	
	constructor (nombreUsuario: string, correo: string, nombre: string, apellidos: string){
		this.nombreUsuario = nombreUsuario;
		this.correo = correo;
		this.nombre = nombre;
		this.apellidos = apellidos;
	}
}
